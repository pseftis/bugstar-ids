@app.route('/api/verify-otp', methods=['POST'])
def verify_otp():
    data = request.json
    email = data.get('email')
    otp_provided = data.get('otp')

    if not email or not otp_provided:
        return jsonify({"error": "Email and OTP are required"}), 400

    conn = sqlite3.connect(DB_FILE)
    c = conn.cursor()
    
    c.execute('SELECT code, expires_at, attempts FROM otp_codes WHERE email = ?', (email,))
    row = c.fetchone()

    if not row:
        conn.close()
        return jsonify({"error": "No OTP requested for this email"}), 404

    stored_code, expires_at, attempts = row

    if time.time() > expires_at:
        # OTP Expired
        c.execute('DELETE FROM otp_codes WHERE email = ?', (email,))
        conn.commit()
        conn.close()
        return jsonify({"error": "OTP has expired. Please request a new one."}), 401
    
    if attempts >= 3:
        # Rate limited
        c.execute('DELETE FROM otp_codes WHERE email = ?', (email,))
        conn.commit()
        conn.close()
        return jsonify({"error": "Too many failed attempts. OTP revoked."}), 401

    if stored_code == otp_provided:
        # Success! Clear OTP.
        c.execute('DELETE FROM otp_codes WHERE email = ?', (email,))
        conn.commit()
        conn.close()
        return jsonify({"message": "Verification successful"}), 200
    else:
        # Increment failed attempts
        c.execute('UPDATE otp_codes SET attempts = attempts + 1 WHERE email = ?', (email,))
        conn.commit()
        conn.close()
        return jsonify({"error": "Invalid OTP."}), 401

import socket

@app.route('/api/scan', methods=['POST'])
def run_scan():
    target = request.json.get('target', '127.0.0.1')
    ports_to_test = [21, 22, 80, 443, 3306, 8080]
    results = []
    
    try:
        # Keep it completely safe and simple
        for port in ports_to_test:
            sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
            sock.settimeout(0.5) # Short timeout
            result = sock.connect_ex((target, port))
            
            if result == 0:
                results.append({"port": port, "status": "OPEN ⚠️", "color": "#f83600"})
            else:
                results.append({"port": port, "status": "CLOSED 🛡️", "color": "#27c93f"})
            sock.close()
            
        return jsonify({"target": target, "results": results}), 200
    except Exception as e:
        return jsonify({"error": "Invalid domain or IP format"}), 400

